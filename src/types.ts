export type TContinentKeys = keyof IContinent;
export type TCountryKeys = keyof ICountry;
export type TCurrencyKeys = keyof ICurrency;
export type TTimezoneKeys = keyof ITimezone;
export type TLanguageKeys = keyof ILanguage;
export type TLanguageTagKeys = keyof ILanguageTag;

type Nullable<T> = T | null;

export interface IContinent {
  code?: TContinentCode;
  name: string;
  countries: TCountryCode[];
}

export interface ICountry {
  code?: TCountryCode;
  name: string;
  native: string;
  capital: string;
  continents: TContinentCode[];
  callingCodes: number[];
  currencies: TCurrencyCode[];
  languages: TLanguageCode[];
  timezones: TTimezoneCode[];
  emoji: string;
  flag: ICountryFlag;
}

export interface ICountryFlag {
  png: string;
  svg: string;
}

export interface ICurrency {
  code?: TCurrencyCode;
  number: string;
  name: string;
  symbol: string;
  countries: TCountryCode[];
}

export interface ITimezone {
  code?: TTimezoneCode;
  name: Nullable<string>;
  label: Nullable<string>;
  countries: TCountryCode[];
  utcOffset: number;
  utcOffsetStr: string;
  dstOffset: number;
  dstOffsetStr: string;
  withCountries: boolean;
}

export interface ILanguage {
  code?: TLanguageCode;
  name: string;
  native: string;
  rtl: boolean;
  countries: TCountryCode[];
}

export interface ILanguageTag {
  code?: TLanguageTagCode;
  name: string;
  native: string;
  language: TLanguageCode;
  country: TCountryCode;
  rtl: boolean;
}

export type TContinents = Record<TContinentCode, IContinent>;
export type TCountries = Record<TCountryCode, ICountry>;
export type TCurrencies = Record<TCurrencyCode, ICurrency>;
export type TTimezones = Record<TTimezoneCode, ITimezone>;
export type TLanguages = Record<TLanguageCode, ILanguage>;
export type TLanguageTags = Record<TLanguageTagCode, ILanguageTag>;
